# Problema01
# Escreva um programa que retorne o salário/hora
# com base no salário mensal e carga horaria
salario_mensal = input('Qual é o seu salário mensal?')
carga_horaria = input('Qual sua carga horária de trabalho?')
salario_hora = int (salario_mensal) / int (carga_horaria)
print (salario_hora)